// Include the input and output library
#include <iostream>

// Allows the use of standard names like cout without std::
using namespace std;

// Main function where program execution begins
int main() 
{
    // for loop starts with i = 0, runs while i < 2, and increases i by 1 each time
    for (int i = 0; i < 2; i++) // printed my name waqar 2 times
    {
        // Print the name and move to the next line
        cout << "waqar" << "\n"; // \n use for new line
    }

    // Return 0 means program finished successfully
    return 0;
}
